type MultichekerLinter struct {
	// checkers содержит список экземпляров, которые будут
	// использоваться для проверки кода.
	checkers []*analysis.Analyzer
}

func main() {
	checkers := []*analysis.Analyzer{
		assign.Analyzer,
		atomic.Analyzer,
		atomicalign.Analyzer,
		bools.Analyzer,
		composite.Analyzer,
		copylock.Analyzer,
		deepequalerrors.Analyzer,
		directive.Analyzer,
		errorsas.Analyzer,
		httpresponse.Analyzer,
		ifaceassert.Analyzer,
		loopclosure.Analyzer,
		lostcancel.Analyzer,
		nilfunc.Analyzer,
		nilness.Analyzer,
		reflectvaluecompare.Analyzer,
		shadow.Analyzer,
		shift.Analyzer,
		sigchanyzer.Analyzer,
		sortslice.Analyzer,
		stdmethods.Analyzer,
		stringintconv.Analyzer,
		structtag.Analyzer,
		tests.Analyzer,
		timeformat.Analyzer,
		unmarshal.Analyzer,
		unreachable.Analyzer,
		unsafeptr.Analyzer,
		unusedresult.Analyzer,
		unusedwrite.Analyzer,
	}

	// Добавление анализаторов из внешних пакетов.
	for _, analyzers := range [][]*lint.Analyzer{
		simple.Analyzers,
		stylecheck.Analyzers,
		quickfix.Analyzers,
		staticcheck.Analyzers,
	} {
		for _, v := range analyzers {
			checkers = append(checkers, v.Analyzer)
		}
	}

	// ДОбавление собственного анализатора.
	checkers = append(checkers, NoOsExitAnalyzer)
}
